let num1= 10;
let num2= 40;
let operador = "*"

switch (operador){
    case operador = "-": console.log(num1-num2);
    break
    case operador = "+": console.log(num1+num2);
    break
    case operador = "*": console.log(num1*num2);
    break
    case operador = "/": console.log(num1/num2);
    break
    default:console.log("Não é possível realizar a operação ");
    break
}