let dia = 6;

switch (dia){
    case (dia = 2): console.log("Segunda-feira");
    break
    case (dia = 3):console.log("Terça-feira");
    break
    case (dia = 4):console.log("Quarta-feira");
    break
    case (dia = 5):console.log("Quinta-feira");
    break
    case (dia = 6):console.log("Sexta-feira");
    break
    case (dia = 7):console.log("Sábado");
    break
    case (dia = 1):console.log("Domingo");
    break
    default: console.log("Não é valido ");
    break
}