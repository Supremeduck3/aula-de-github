let estação = 4;

switch(estação){
    case 1: 
    console.log("Verão");
    case 2: 
    console.log("Outono");
    break
    case 3: 
    console.log("Inverno");
    break
    case 4: 
    console.log("Primavera");
    break
    default:
    console.log("Esse caractere não é valido coloque um valor de 1-4");
}